#include "libft.h"

int	ft_is_in_min_max_atoi_long(const char *str)
{
	long	i[4];

	i[0] = 0;
	i[1] = 1;
	i[2] = 0;
	i[3] = 0;
	if (!ft_strcmp(str, "-9223372036854775808"))
		return (1);
	while (((9 <= str[i[0]] && str[i[0]] <= 13) || str[i[0]] == 32)
		&& str[i[0]])
		i[0]++;
	if ((str[i[0]] == '-' || str[i[0]] == '+') && str[i[0]])
	{
		(str[i[0]] == '-') ? i[1] = -i[1] : 0;
		i[0]++;
	}
	while (('0' <= str[i[0]] && str[i[0]] <= '9') && str[i[0]])
	{
		i[2] = i[2] * 10 + (str[i[0]] - '0');
		if (i[2] < i[3])
			return (0);
		i[3] = i[2];
		i[0]++;
	}
	return (1);
}
