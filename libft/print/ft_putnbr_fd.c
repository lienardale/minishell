#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int	a;

	a = (n > 0) ? n : -n;
	(n < 0) ? write(fd, "-", 1) : 0;
	(a > 9) ? ft_putnbr_fd((int)(a / 10), fd) : 0;
	ft_putchar_fd(a % 10 + '0', fd);
}
